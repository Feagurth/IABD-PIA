# Escribe un programa que pida la base y la altura de un rectángulo en milímetros y muestre el área del mismo.

base = input("Introduzca la longitud de la base en milímetros: ")
altura = input("Introduzca la longitud de la altura en milímetros: ")
area = (int(base) * int(altura))/2

print("El area del triángulo es: ", area)